
public class Java_01_변수 {

	public static void main(String[] args) {
		// 변수
		// - 변수란 값을 저장할 수 있는 메모리의 공간을 의미한다.
		
		// 변수의 선언
		// - 타입 변수이릅;
		int count; // (정수 값을 담을 수 있는 count라는 이름의 변수를 선언)
		double average; // (실수 갑을 담을 수 있는 average 라는 변수를 선언)
		// 명명규칙
		// - 변수의 이름을 정할 때 자바 언어에서 정해놓은 규칙
		// 식별자(identifier)는 클래스, 메소드, 변수 등 다양한 대상에 대해 이름이 붙여지는 경우. 그 이름을 뜻한다.
		
		// java 식별자 명명규칙
		// - 하나 이상의 글자로 이루어져야 함
		// - 첫 번째 글자는 문자 이거나 '$','_'이어야 함
		// - 두 번째 이후의 글자는 숫자, 문자, '$','_'이어야 함
		// - '$','_'이외의 특수문자 사용 불가능
		// - 길이 제한이 없음
		// - 키워드는 식별자로 사용할 수 없음
		// - 상수 값을 표현하는 단어 true, false, null은 식별자로 사용할 수 없음
		
		// 변수 명명 관례
		// - 첫 번쨰 문자가 소문자인 명사로 정함
		// - 여러 단어로 구성된 이르므이 경우 두 번쨰 단어부터 첫글자를 대문자로 함
		// - '_'을 쓰지 않음
		// * 규칙은 반드시 지켜야 한다. 지키지 않으면 컴파일시 에러가 발생함. *
		// * 관례는 지키지 않아도 컴파일에러가 발생하지는 않지만, 약속이기 때문에 지켜주는것이 좋다. *
		
		// 변수의 사용
		//  변수명 = 값;
		count = 10; // (count라는 int형 변수에 정수값 10을 담습니다.)
		count = 20; // (count라는 int형 변수에 정수값 20을 담습니다. 원래 담겨있던 10은 없어집니다. 변수는 값이 얼마든지 변할 수 있습니다.)
		average = 11.1; // (average라는 double형 변수에 실수 값 11.1을 담습니다.)
		
	}

}
